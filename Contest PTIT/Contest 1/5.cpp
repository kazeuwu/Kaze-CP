int a[100];
void backtrack(int i, int n, int k) {
    for(int j = a[i - 1] + 1; j <= n - k + i; j++) {
        a[i] = j;
        if(i == k) {
            for(int x = 1; x <= k; x++) cout << a[x];
            cout << " ";
        }
        else backtrack(i + 1, n, k);
    }
}

void solve() {
    int t; cin >> t;
    while(t--) {
        int n, k; cin >> n >> k;
        for(int i = 1; i <= k; i++) a[i] = i;
        backtrack(1, n, k);
        cout << endl;
    }
}

