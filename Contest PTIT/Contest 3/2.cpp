string akira(string a, string b) {
    ll carry = 0, tmp;
    for(int i = 0; i < max(a.size(), b.size()); i++) {
        if(a.size() == b.size()) break;
        a = a.size() < b.size() ? '0' + a : a;
        b = b.size() < a.size() ? '0' + b : b;
    }
    for(int i = a.size() - 1; i >= 0; i--) {
        tmp = a[i] - '0' + b[i] - '0' + carry;
        carry = tmp / 10;
        a[i] = (tmp % 10) + '0';
    }
    a = carry == 1 ? '1' + a : a;
    return a;
}
