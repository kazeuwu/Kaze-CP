void solve() {
    int t; cin >> t;
    while(t--) {
        int n, s; cin >> n >> s;
        int sum = 0, mx = -1, l = 0;
        int a[200001];
        for(int i = 0; i < n; i++) {
            cin >> a[i];
            sum += a[i];
            while(sum > s) {
                sum -= a[l];
                ++l;
            }
            if(sum == s) mx = max(mx, i - l + 1);
        }
        cout << (mx == -1 ? -1 : n - mx) << endl;
    }
}

