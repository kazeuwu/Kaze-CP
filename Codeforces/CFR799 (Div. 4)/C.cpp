void solve() {
    int t; cin >> t;
    while(t--) {
        string a[8];
        for(int i = 0; i < 8; i++) {
            string s; cin >> s;
            a[i] = s;
        }
        int x, y;
        bool ok = false;
        for(int i = 0; i < 8; i++) {
            int c = 0;
            for(int j = 0; j < 8; j++) {
                if(a[i][j] == '#') {
                    c++;
                    y = j + 1;
                }
            }
            if(c == 2) {
                ok = true;
            }
            if(ok == true && c == 1) {
                x = i + 1;
                break;
            }
        }
        cout << x << " " << y << endl;
    }
}

