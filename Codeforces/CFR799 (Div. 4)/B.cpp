void solve() {
    int t; cin >> t;
    while (t--) {
        set<ll> s;
        ll n; cin >> n;
        for(int i = 0, tmp; i < n; i++) {
            cin >> tmp;
            s.insert(tmp);
        }
        ll sz = s.size();
        ll x = n - sz;
        if(x & 1) cout << sz - 1;
        else cout << sz;
        cout << endl;
    }
}

