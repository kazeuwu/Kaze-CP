void solve() {
    int t; cin >> t;
    while(t--) {
        string s; cin >> s;
        int n; cin >> n;
        ll hour = (s[0] - '0') * 10 + (s[1] - '0');
        ll min = (s[3] - '0') * 10 + (s[4] - '0');
        ll h = hour, m = min, cnt = 0;
        while(true) {
            min += n;
            hour += (min / 60);
            min %= 60;
            hour %= 24;
            ll a = hour % 10, aa = (hour / 10) % 10;
            ll b = min % 10, bb = (min / 10) % 10;
            if(a == bb && aa == b) ++cnt;
            if(hour == h && min == m) break;
        }
        cout << cnt << endl;
    }
}

