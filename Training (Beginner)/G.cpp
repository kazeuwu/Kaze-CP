#include <bits/stdc++.h>
typedef long long ll;
typedef long double ldb;
typedef unsigned long long ull;
using namespace std;

// Problem G
string cut(string &x) {
    for(int i = 0; i < x.size(); i++) {
        if(x[i] == '0') {
            x.erase(i, 1);
        }
    }
    return x;
}

void solve() {
    char s[100001]; cin >> s;
    char tmp[100001];
    vector<string> v;
    vector<ll> finish;
    for(int i = 0, cnt = 0; i <= strlen(s); i++){
        if(isdigit(s[i])) {
            tmp[cnt++] = s[i];
            tmp[cnt] = '\0';
        }
        else if(cnt) {
            v.push_back(tmp);
            cnt = 0;
        }
    }

    for(int i = 0; i < v.size(); i++) {
        cut(v[i]);
        finish.push_back(stoll(v[i]));
    }
    ll max = 0, min = finish[0];
    for(int i = 0; i < finish.size(); i++) {
        if(finish[i] > max) max = finish[i];
        if(finish[i] < min) min = finish[i];
    }
    cout << min << endl << max;
}

int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    solve();
    return 0;
}

