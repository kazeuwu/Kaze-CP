#include <bits/stdc++.h>
typedef long long ll;
typedef long double ldb;
typedef unsigned long long ull;
using namespace std;

// Problem F
ll n, m;
ll h[1000005];

bool ok(ll x) {
    ll sum = 0;
    for(int i = 0; i < n; i++) {
        if(h[i] > x) sum += h[i] - x;
    }
    if(sum >= m) return true;
    return false;
}

void solve() {
    cin >> n >> m;
    for(int i = 0; i < n; i++) cin >> h[i];
    sort(h, h + n);
    ll l = 0, r = 1000000000, mid;
    while(l < r) {
        mid = (l + r) / 2;
        if(ok(mid)) l = mid + 1;
        else r = mid - 1; 
    }
    cout << l;
}
int main() {
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);
    solve();
    return 0;
}

